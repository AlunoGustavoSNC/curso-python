# Tipos primitivos

# Inteiro

n1 = int(8)
n2 = int(9)

soma = n1 + n2

print(f"A soma é desses números é {soma}")

# Float

n1 = float(4.5)
n2 = float(7.4)

soma = n1 + n2

print("A soma é desses números é {}".format(soma))

# String

nome = 'Isso é uma string'

