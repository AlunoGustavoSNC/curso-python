# Operadores Aritméticos

# Igualdade: ==
print('Igualdade: ==')

print( 2 == 2 )

# Somar: +
print('# Somar: +')

print( 1 + 1 )

# Subtrair: -
print('Subtrair: -')

print( 1 - 1 )

# Multiplicar: *
print('Multiplicar: *')

print( 2 * 2 )

# Dividir: /
print('Dividir: /')

print( 7 / 2 )

# Divisão inteira: //
print('Divisão inteira: //')

print( 7 // 2)

# Resto da divisão: %
print('Resto da divisão: %')

print( 7 % 2 )

# Ordem de Precedência

# 1º: ()
# 2º: **
# 3º: *  /  //  %
# 4º: +  -